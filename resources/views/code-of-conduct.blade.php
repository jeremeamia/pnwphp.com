                <p>
                    The Pacific Northwest PHP conference strives to be inclusive to the largest number of attendees
                    and speakers as possible. We are committed to creating and maintaining a friendly, safe, and welcoming
                    environment for all. We invite everyone who attends to help us in achieving that goal.
                </p>
                <p>
                    This Code of Conduct (CoC) outlines the expectations of all who attend,
                    speak at, or work at the conference, as well as how we will respond to unacceptable behavior.
                    We expect everyone will abide by this CoC in all conference venues whether online or in-person,
                    as well as in one-on-one communications.
                </p>

                <h2>Expectations</h2>

                <p>
                    You are expected to:
                </p>
                <ul>
                    <li>Behave professionally.</li>
                    <li>Refrain from any unacceptable behavior.</li>
                    <li>Exercise consideration and respect in your speech and actions.</li>
                    <li>Attempt collaboration before conflict.</li>
                    <li>Respect others views, beliefs, and personal space.</li>
                </ul>
                <p>
                    Unacceptable behaviors include: intimidating, harassing, abusive, discriminatory, derogatory,
                    or demeaning speech or actions. Conference event venues may be shared with members of the public;
                    please be respectful to all patrons of these locations.
                </p>
                <p>
                    Harassment includes: harmful or prejudicial verbal or written comments related to gender,
                    sexual orientation, race, religion, disability; inappropriate use of nudity and/or sexual
                    images in public spaces (including presentation slides), deliberate intimidation, stalking
                    or following; harassing photography or recording, unwanted photography or recording,
                    sustained disruption of talks or other events, inappropriate physical contact, and unwelcome sexual attention.
                </p>
                <p>
                    Our expectation is that we will treat each other with consideration, empathy, and respect.
                    Our speech and behavior will be guided by a desire to create and maintain an inclusive
                    community purposed towards sharing knowledge and encouraging enthusiasm for solving problems with PHP.
                </p>

                <h2>Consequences of Unacceptable Behavior</h2>
                <p>
                    Unacceptable behavior from any attendee, including sponsors and those with decision-making authority, will not be tolerated.
                </p>
                <p>
                    Anyone asked to stop unacceptable behavior is expected to comply immediately. If an attendee engages in
                    unacceptable behavior, the conference organizers may take any action they deem appropriate, up to and including
                    a temporary ban or permanent expulsion from the conference without warning (and without refund).
                </p>

